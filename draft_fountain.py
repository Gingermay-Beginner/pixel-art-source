from PIL import Image

S = 12
W, H = 64*S, 36*S
img = Image.new('RGB', (W, H), (180, 200, 225))
pixels = img.load()

def sp(x, y, c):
    if 0 <= x < 64 and 0 <= y < 36:
        for dy in range(S):
            for dx in range(S):
                pixels[x*S+dx, y*S+dy] = c

def fl(y1, y2, x1, x2, c):
    for y in range(y1, y2+1):
        for x in range(x1, x2+1):
            sp(x, y, c)

def wrow(y, x1, x2, c):
    for x in range(x1, x2+1):
        sp(x, y, c)

def wcol(x, y1, y2, c):
    for y in range(y1, y2+1):
        sp(x, y, c)

